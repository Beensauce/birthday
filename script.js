const display = document.getElementById('ascii-display');
// 1. Split the art into an array of lines
const lines = asciiArt.split('\n').filter(line => line.length > 0); // Remove empty lines
let currentLine = 0;


function createConfetti() {
    const confettiCount = 500;
    const colors = ['#ff6b6b', '#4ecdc4', '#45b7d1', '#ffd93d', '#a8e6cf', '#ff8c94', '#ffd1dc'];
    
    for (let i = 0; i < confettiCount; i++) {
        const confetti = document.createElement('div');
        confetti.className = 'confetti';
        confetti.style.left = Math.random() * window.innerWidth + 'px';
        confetti.style.top = 0;
        confetti.style.backgroundColor = colors[Math.floor(Math.random() * colors.length)];
        confetti.style.width = (Math.random() * 10 + 5) + 'px';
        confetti.style.height = confetti.style.width;
        confetti.style.borderRadius = '50%';
        confetti.style.setProperty('--delay', (Math.random() * 0.3) + 's');
        confetti.style.animationDelay = confetti.style.getPropertyValue('--delay');
        document.body.appendChild(confetti);
        
        setTimeout(() => confetti.remove(), 3000);
    }
}

function revealLines() {
    if (currentLine < lines.length) {
        // 2. Add the full line plus a newline character
        display.textContent += lines[currentLine] + '\n';
        currentLine++;
        
        // 3. Keep scrolling to the bottom
        window.scrollTo(0, document.body.scrollHeight);
        
        // 4. Set a delay between lines
        setTimeout(revealLines, 15); 
    }
    else {
        
        text.classList.toggle('hidden');
        text.classList.add('fade-in');
        sub.classList.toggle('hidden');
        sub.classList.add('fade-in');
        createConfetti();
        window.scrollTo(0, document.body.scrollHeight);
    }
}

revealLines();